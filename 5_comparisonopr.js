console.log(1==1) // check 1 is equal to 1
console.log(1==2) // check 1 is equal to 2
console.log(1>2) // check 1 is greater than 2
console.log(1<2) // check 1 is less than 2

console.log(1>=2) // check 1 is greater than or equal to 2
console.log(1<=2) // check 1 is less than or equal to 2

console.log(1!=2) // check 1 is not equal to 2

//strict comparison here it checks even datatype 
console.log(1==1) //true
console.log("1"==1)// true
console.log("1"===1)//false because 1 is string and other is int number


 console.log(1===1) // check 1 is strictly equal to 1
 console.log(1===2) // check 1 is strictly equal to 2

